public class Exercicio1 {

    /*Um professor deseja calcular a média de um aluno. 
    O professor tem o nome e as 3 notas do aluno e a média 
    deve ser calculada de forma aritmética*/

    public static void main(String args[]) {

        float nota1 = 7;
        float nota2 = 5;
        float nota3 = 9.5f;

        float media = (nota1 + nota2 + nota3) /3;

        System.out.println("A média do aluno é = " + media);    
    }
}